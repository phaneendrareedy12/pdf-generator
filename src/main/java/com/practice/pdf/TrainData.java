package com.practice.pdf;

public class TrainData {

    private String train;
    private String day;
    private String units;
    private String status;
    private String delay;
    private String estTrainArrival;
    private String event;
    private String dateAndTime;
    private String location;
    private String originTrain;
    private String destinationTrain;
    private String finalDestination;

    public String getTrain() {
        return train;
    }

    public void setTrain(String train) {
        this.train = train;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getUnits() {
        return units;
    }

    public void setUnits(String units) {
        this.units = units;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDelay() {
        return delay;
    }

    public void setDelay(String delay) {
        this.delay = delay;
    }

    public String getEstTrainArrival() {
        return estTrainArrival;
    }

    public void setEstTrainArrival(String estTrainArrival) {
        this.estTrainArrival = estTrainArrival;
    }

    public String getEvent() {
        return event;
    }

    public void setEvent(String event) {
        this.event = event;
    }

    public String getDateAndTime() {
        return dateAndTime;
    }

    public void setDateAndTime(String dateAndTime) {
        this.dateAndTime = dateAndTime;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getOriginTrain() {
        return originTrain;
    }

    public void setOriginTrain(String originTrain) {
        this.originTrain = originTrain;
    }

    public String getDestinationTrain() {
        return destinationTrain;
    }

    public void setDestinationTrain(String destinationTrain) {
        this.destinationTrain = destinationTrain;
    }

    public String getFinalDestination() {
        return finalDestination;
    }

    public void setFinalDestination(String finalDestination) {
        this.finalDestination = finalDestination;
    }
}
